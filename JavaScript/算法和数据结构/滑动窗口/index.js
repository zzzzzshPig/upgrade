//1004. 最大连续1的个数 III
var longestOnes = function(A, K) {
    let r = 0
    let l = 0

    let max = 0
    let res = 0
    while (r < A.length) {
        const c = A[r]
        r++

        if (c !== 1) K--
        if (K >= -1) max++

        if (K <= -1) {
            const d = A[l]
            l++

            max--
            res = Math.max(res, max)

            if (d === 0) K++
        }
    }

    return Math.max(res, max)
}
